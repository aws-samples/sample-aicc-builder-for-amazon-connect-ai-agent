def _generate_iam_policy(spec: OperationSpec) -> str:
    """Generate IAM policy for the Lambda function."""
    import json

    statements = []

    # DynamoDB permissions
    if spec.data_source.db_type == "dynamodb":
        actions = []
        if spec.operation_type == "create":
            actions = ["dynamodb:PutItem"]
        elif spec.operation_type == "read":
            actions = ["dynamodb:GetItem"]
        elif spec.operation_type == "update":
            actions = ["dynamodb:UpdateItem"]
        elif spec.operation_type == "delete":
            actions = ["dynamodb:DeleteItem"]
        elif spec.operation_type in ("list", "search"):
            actions = ["dynamodb:Scan", "dynamodb:Query"]

        statements.append({
            "Sid": "DynamoDBAccess",
            "Effect": "Allow",
            "Action": actions,
            "Resource": [
                f"arn:aws:dynamodb:*:*:table/{spec.data_source.table_name}",
                f"arn:aws:dynamodb:*:*:table/{spec.data_source.table_name}/index/*"
            ]
        })

    # RDS Data API permissions
    elif spec.data_source.db_type in ("rds_mysql", "rds_postgresql"):
        statements.append({
            "Sid": "RDSDataAPIAccess",
            "Effect": "Allow",
            "Action": [
                "rds-data:ExecuteStatement",
                "rds-data:BatchExecuteStatement"
            ],
            "Resource": "*"
        })
        statements.append({
            "Sid": "SecretsManagerAccess",
            "Effect": "Allow",
            "Action": ["secretsmanager:GetSecretValue"],
            "Resource": spec.data_source.connection_secret_arn or "*"
        })

    # CloudWatch Logs
    statements.append({
        "Sid": "CloudWatchLogs",
        "Effect": "Allow",
        "Action": [
            "logs:CreateLogGroup",
            "logs:CreateLogStream",
            "logs:PutLogEvents"
        ],
        "Resource": "arn:aws:logs:*:*:*"
    })

    # SES for email side effects
    for effect in spec.side_effects:
        if effect.effect_type == "email":
            statements.append({
                "Sid": "SESAccess",
                "Effect": "Allow",
                "Action": ["ses:SendEmail", "ses:SendRawEmail"],
                "Resource": "*"
            })
            break

    policy = {
        "Version": "2012-10-17",
        "Statement": statements
    }

    return json.dumps(policy, indent=2)


