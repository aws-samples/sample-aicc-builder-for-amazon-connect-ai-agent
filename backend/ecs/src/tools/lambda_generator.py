def _get_side_effect_imports(side_effects: list) -> str:
    """Get imports for side effects."""
    imports = set()
    for effect in side_effects:
        if effect.effect_type in ("email", "sms"):
            imports.add("from botocore.exceptions import ClientError")
        if effect.effect_type == "email":
            imports.add("# SES client for sending emails\nses_client = boto3.client('ses')")
        if effect.effect_type == "sms":
            imports.add("# SNS client for SMS\nsns_client = boto3.client('sns')")

    return "\n".join(imports)


def _generate_field_extraction(fields: list[FieldSpec]) -> str:
    """Generate code to extract fields from request body."""
    lines = ["        # Extract input fields", "        input_data = {}"]

    for field in fields:
        if field.required:
            lines.append(f'        input_data["{field.name}"] = body.get("{field.name}")')
        else:
            default = f'"{field.default_value}"' if field.default_value else "None"
            lines.append(f'        input_data["{field.name}"] = body.get("{field.name}", {default})')

    return "\n".join(lines)


